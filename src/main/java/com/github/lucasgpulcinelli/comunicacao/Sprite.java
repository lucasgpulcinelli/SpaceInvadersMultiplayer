package com.github.lucasgpulcinelli.comunicacao;

/**
 * Sprite representa uma entidade a ser desenhada na tela, representando uma das
 * classes não abstratas em {@link sistema}
 */
public enum Sprite {
    /** sprite do canhao / jogador */
    CANHAO,

    /** sprite de um tiro tanto do canhao quanto de um inimigo */
    TIRO,

    /** sprite do primeiro tipo de inimigo */
    INIMIGO1,

    /** sprite do segundo tipo de inimigo */
    INIMIGO2,

    /** sprite do segundo tipo de inimigo */
    INIMIGO3,

    /** sprite da base com vida 1 */
    BASE1,
    /** sprite da base com vida 2 */
    BASE2,
    /** sprite da base com vida 3 */
    BASE3,
    /** sprite da base com vida 4 */
    BASE4,
    /** sprite da base com vida 5 */
    BASE5,

    /** sprite da nave especial */
    NAVEESPECIAL,

    /** sprite da particula de destruição de objetos */
    PARTICULA,
}
